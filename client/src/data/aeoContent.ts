export function getAEOPage(type: AEOPage["type"], slug: string): AEOPage | undefined {
  return AEO_PAGES.find((p) => p.type === type && p.slug === slug);
}

export function getAEOPagesByType(type: AEOPage["type"]): AEOPage[] {
  return AEO_PAGES.filter((p) => p.type === type);
}
